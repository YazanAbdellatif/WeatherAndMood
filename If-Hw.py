    t.backward(70)
    t.color("black")
    t.begin_fill()
    t.pendown()
    t.circle(30)
    t.end_fill()
    t.penup()
    t.goto(-100,100)
    t.left(110)
    t.pendown()
    t.circle(100,150)
    t.hideturtle()
    turtle.done()
