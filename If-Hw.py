weather=input("what is the weather for you today sunny, rainy, cloudy, or stormy? ")#asking weather
mood=input("what is your mood today happy, sad , angry, normal? ")#asking mood
if weather=="sunny":
    print("Nice! you should have a walk outside")#replies
elif weather=="rainy":
    print("Oh make sure to use an umbrella")
elif weather=="cloudy":
    print("Oh perfect temperature not too hot or cold!")
elif weather=="stormy":
    print("Make sure to stay inside since its dangerous!")

if mood==("happy"):#replies for moods
    import turtle
    t=turtle.Turtle()
    t.speed(0)
    t.color("yellow")
    t.begin_fill()
    t.goto(0,0)
    t.circle(150)
    t.end_fill()
    t.goto(0,200)
    t.left(180)
    t.forward(70)
    t.color("black")
    t.begin_fill()
